"""
列表访问的两种方法
（1）单个元素访问
正向访问
负向访问
（2）切⽚访问
"""

a = [3, 7, 4, 2, 6]
print(a[1])  # 7
print(a[-4])  # 7
a1 = a[0:3]  # 等价于a[0:3:1]
print(a1)  # [3, 7, 4]
a2 = a[0:3:1]
print(a2)  # [3, 7, 4]
print(a[-3:])  # [4, 2, 6]
print(a[-3:-1])  # [4, 2]
print(a[0:5:2])  # [3, 4, 6]
print(a[::2])   # a[::2]等价于a[0:5:2]  输出 [3, 4, 6]
