"""
思考题：舞会派对
已知参加舞会的⼈员名单如下，⼈数为偶数：
['张虹','任明', '李胜男', '⽂丽', '楚明', '任少芬']
根据相邻配对的⽅法，返回下⾯⼀个列表：
[(张虹','任明'), ('李胜男', '⽂丽'), ('楚明', '任少芬')]
"""

a = ['张虹', '任明', '李胜男', '⽂丽', '楚明', '任少芬']
a1 = a[:-1]
a2 = a[1:]

result = []
for i, j in zip(a1, a2):
    result.append((i, j))
print(result)

print(result[::2])
